#include <kernel/synchronic_api.h>
#include <kernel/spinlock.h>
#include <arch/thread.h>

 #if WITH_LIB_SM
#define PROTOCOL_LOCK_FLAGS SPIN_LOCK_FLAG_IRQ_FIQ
 #else
#define PROTOCOL_LOCK_FLAGS SPIN_LOCK_FLAG_INTERRUPTS
#endif


void init(unsigned long *lock)
{
	return spin_lock_init((spin_lock_t *)lock);
}

void enter_disable_interrupt(unsigned long *lock, unsigned int *statep)
{
	return spin_lock_save((spin_lock_t *)lock, (spin_lock_saved_state_t *)statep, PROTOCOL_LOCK_FLAGS);
}

void exit_enable_interrupt(unsigned long *lock, unsigned int old_state)
{
	return spin_unlock_restore((spin_lock_t *)lock, (spin_lock_saved_state_t)old_state, PROTOCOL_LOCK_FLAGS);
}